
DROP TABLE IF EXISTS picks;
DROP TABLE IF EXISTS matches;
DROP TABLE IF EXISTS teams;
DROP TABLE IF EXISTS users;

CREATE TABLE teams
(
    rowid INTEGER PRIMARY KEY,
    name TEXT NOT NULL 
);

CREATE TABLE users
(
  rowid INTEGER PRIMARY KEY,
  name TEXT NOT NULL,
  password TEXT NOT NULL,
  salt TEXT NOT NULL
);

CREATE TABLE matches
(
  rowid INTEGER primary key,
  week INTEGER NOT NULL,
  time INTEGER NOT NULL,
  home_team INTEGER NOT NULL,
  away_team INTEGER NOT NULL,
  CONSTRAINT fk_away_team_to_teams FOREIGN KEY (away_team)
      REFERENCES teams (rowid) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT fk_home_team_to_teams FOREIGN KEY (home_team)
      REFERENCES teams (rowid) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE picks
(
  rowid INTEGER PRIMARY KEY,
  match_id INTEGER NOT NULL,
  pick_ishome BOOLEAN NOT NULL,
  user_id INTEGER NOT NULL,
  CONSTRAINT fk_match_id_to_matches FOREIGN KEY (match_id)
      REFERENCES matches (rowid) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT fk_user_id_to_users FOREIGN KEY (user_id)
      REFERENCES users (rowid) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT uq_match_user UNIQUE (match_id, user_id)
);

